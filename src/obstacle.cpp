Obstacle::Obstacle(Vector2 position) : position(position) {
    for (u32 row = 0; row < grid.size(); row++) {
        for (u32 column = 0; column < grid[0].size(); column++) {
            if (grid[row][column] == 1) {
                float pos_x = position.x + column * 3; // size of block
                float pos_y = position.y + row * 3;
                Block block = Block({pos_x, pos_y});
                blocks.push_back(block);
            }
        }
    }
}

